const WATCHDOG_SCRIPT = (p: Platform, c: Config) => `#!/bin/bash
# relay-watchdog — generated by nostr-station
# Throwaway identity — rotate freely with: nak keygen

RELAY_PORT=8080
WATCHDOG_NSEC="${c.watchdogNsec ?? ''}"
YOUR_NPUB="${c.npub}"
FALLBACK_RELAY="${c.fallbackRelays.split(' ')[0]}"

if ! nc -z localhost $RELAY_PORT 2>/dev/null; then
    TIMESTAMP=$(date '+%Y-%m-%d %H:%M:%S')
    MESSAGE="⚠️ nostr-station relay is down as of $TIMESTAMP"
    nak event --sec "$WATCHDOG_NSEC" -k 4 -p "$YOUR_NPUB" --content "$MESSAGE" "$FALLBACK_RELAY"
    echo "$TIMESTAMP — relay down, DM sent" >> ~/logs/watchdog.log
else
    echo "$(date '+%Y-%m-%d %H:%M:%S') — relay OK" >> ~/logs/watchdog.log
fi
`;

