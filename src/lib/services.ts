export function writeContextFile(p: Platform, c: Config): string {
  const canonicalPath = `${p.projectsDir}/${CONTEXT_FILENAME}`;
  write(canonicalPath, buildContextContent(p, c));
  symlinkEditorFile(p, c.editor);
  return canonicalPath;
}

// Creates a symlink from the tool-specific filename → NOSTR_STATION.md
// Safe to call multiple times — removes old symlink before creating new one
export function symlinkEditorFile(p: Platform, editor: string): string {
  const filename = EDITOR_FILENAMES[editor] ?? EDITOR_FILENAMES['other'];
  const canonicalPath = `${p.projectsDir}/${CONTEXT_FILENAME}`;
  const linkPath = `${p.projectsDir}/${filename}`;

  // Handle subdirectory (e.g. .github/copilot-instructions.md)
  fs.mkdirSync(path.dirname(linkPath), { recursive: true });

  // Remove existing file or symlink at target
  try { fs.unlinkSync(linkPath); } catch {}

  // Relative symlink so it works if the projects dir is moved
  fs.symlinkSync(CONTEXT_FILENAME, linkPath);
  return linkPath;
}

export function generateWatchdogKeypair(cargoBin: string): { nsec: string; npub: string } {
  try {
    const out = execSync(`${cargoBin}/nak keygen`, { stdio: 'pipe' }).toString();
    const nsec = out.match(/nsec[a-z0-9]+/)?.[0] ?? '';
    const npub = out.match(/npub[a-z0-9]+/)?.[0] ?? '';
    return { nsec, npub };
  } catch {
    return { nsec: '', npub: '' };
  }
}
