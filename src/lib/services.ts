function write(filePath: string, content: string, mode = 0o644) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, content, { mode });
}

function shell(cmd: string) {
  try { execSync(cmd, { stdio: 'pipe' }); } catch {}
}

export function writeRelayConfig(p: Platform, c: Config) {
  const dest = `${p.configDir}/config.toml`;
  if (fs.existsSync(dest)) fs.copyFileSync(dest, `${dest}.bak`);
  write(dest, RELAY_CONFIG(p, c));
}

export function writeWatchdogScript(p: Platform, c: Config) {
  const dest = `${p.scriptsDir}/relay-watchdog.sh`;
  write(dest, WATCHDOG_SCRIPT(p, c), 0o700);
}

export function setupDirs(p: Platform) {
  for (const d of [p.logDir, p.scriptsDir, p.projectsDir, p.relayDataDir]) {
    fs.mkdirSync(d, { recursive: true });
  }
}

export function installRelayService(p: Platform) {
  if (p.serviceBackend === 'launchd') {
    const dest = `${p.launchAgentsDir}/com.nostr-station.relay.plist`;
    write(dest, LAUNCHD_RELAY(p));
    shell(`launchctl unload "${dest}" 2>/dev/null; launchctl load "${dest}"`);
  } else {
    const unitDir = `${p.homeDir}/.config/systemd/user`;
    write(`${unitDir}/nostr-relay.service`, SYSTEMD_RELAY(p));
    shell('systemctl --user daemon-reload && systemctl --user enable --now nostr-relay.service');
  }
}

export function installWatchdogService(p: Platform) {
  if (p.serviceBackend === 'launchd') {
    const dest = `${p.launchAgentsDir}/com.nostr-station.watchdog.plist`;
    write(dest, LAUNCHD_WATCHDOG(p));
    shell(`launchctl unload "${dest}" 2>/dev/null; launchctl load "${dest}"`);
  } else {
    const unitDir = `${p.homeDir}/.config/systemd/user`;
    write(`${unitDir}/nostr-watchdog.service`, SYSTEMD_WATCHDOG_SERVICE(p));
    write(`${unitDir}/nostr-watchdog.timer`, SYSTEMD_WATCHDOG_TIMER);
    shell('systemctl --user daemon-reload && systemctl --user enable --now nostr-watchdog.timer');
  }
}

export function writeClaudeEnv(homeDir: string, c: Config) {
  if (c.aiProvider === 'anthropic') return;

  let lines: string[];

  switch (c.aiProvider) {
    case 'openrouter':
      lines = [
        `export ANTHROPIC_API_KEY="${c.openrouterKey}"`,
        `export ANTHROPIC_BASE_URL="https://openrouter.ai/api/v1"`,
        `export CLAUDE_MODEL="${c.openrouterModel ?? 'anthropic/claude-sonnet-4'}"`,
      ];
      break;

    case 'routstr':
      // Routstr: pay-per-use via Cashu token (Lightning/Bitcoin)
      // Compatible with OpenRouter API format
      lines = [
        `export ANTHROPIC_API_KEY="${c.routstrCashuToken}"`,
        `export ANTHROPIC_BASE_URL="${c.routstrServer ?? 'https://api.routstr.com'}"`,
        `export CLAUDE_MODEL="anthropic/claude-sonnet-4"`,
      ];
      break;

    case 'ppq':
      // PayPerQ: pay-per-query AI — ppq.ai
      lines = [
        `export ANTHROPIC_API_KEY="${c.ppqApiKey}"`,
        `export ANTHROPIC_BASE_URL="https://api.ppq.ai"`,
        `export CLAUDE_MODEL="anthropic/claude-sonnet-4"`,
      ];
      break;

    case 'opencode-zen':
      // OpenCode Zen — curated, coding-optimised models, benchmarked for agents
      // OpenAI-compatible at opencode.ai/zen/v1  |  get key: opencode.ai/auth
      // Models use format: opencode/<model-id>
      lines = [
        `export ANTHROPIC_API_KEY="${c.opencodeZenKey}"`,
        `export ANTHROPIC_BASE_URL="https://opencode.ai/zen/v1"`,
        `export CLAUDE_MODEL="${c.opencodeZenModel ?? 'opencode/claude-opus-4-6'}"`,
      ];
      break;

    case 'maple':
      // Maple Proxy — TEE-encrypted inference, end-to-end private
      // Desktop app default port is 8080, same as our relay — guide users to use 8081
      lines = [
        `export ANTHROPIC_API_KEY="${c.mapleApiKey ?? 'maple-desktop-auto'}"`,
        `export ANTHROPIC_BASE_URL="${c.mapleBase ?? 'http://localhost:8081/v1'}"`,
        `export CLAUDE_MODEL="llama-3.3-70b"`,
      ];
      break;

    case 'ollama':
      // Ollama: local models, no real API key needed
      // SDK requires a non-empty key string — 'ollama' is conventional
      lines = [
        `export ANTHROPIC_API_KEY="ollama"`,
        `export ANTHROPIC_BASE_URL="${c.ollamaBase ?? 'http://localhost:11434/v1'}"`,
        `export CLAUDE_MODEL="${c.ollamaModel ?? 'llama3'}"`,
      ];
      break;

    case 'lmstudio':
      // LM Studio: OpenAI-compatible local server
      lines = [
        `export ANTHROPIC_API_KEY="lm-studio"`,
        `export ANTHROPIC_BASE_URL="${c.lmstudioBase ?? 'http://localhost:1234/v1'}"`,
        `export CLAUDE_MODEL="${c.lmstudioModel ?? 'local-model'}"`,
      ];
      break;

    case 'custom':
    default:
      lines = [
        `export ANTHROPIC_API_KEY="${c.customApiKey}"`,
        `export ANTHROPIC_BASE_URL="${c.customApiBase}"`,
        `export CLAUDE_MODEL="${c.customModel}"`,
      ];
      break;
  }

  write(`${homeDir}/.claude_env`, lines.join('\n') + '\n');
}

